def decompressRLElist(nums):
    decompressed = []
    for i in range(0, len(nums), 2):
        decompressed.extend(nums[i] * [nums[i + 1]])
    return decompressed


def test_1():
    assert decompressRLElist([1, 2, 3, 4]) == [2, 4, 4, 4]


def test_2():
    assert decompressRLElist([4, 6, 2, 4]) == [6, 6, 6, 6, 4, 4]


